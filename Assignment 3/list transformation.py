items = [8, 9, 10]  # Initial value of the list.
items[1] = 17   # Set the second entry (index 1) to 17.
items.extend([4, 5, 6]) # Add 4, 5 and 6 to the end of the list.
items.pop(0)    # Remove the first entry from the list.
items.sort()    # Sort the list
items = 2 * items   # Double the elements of the list.
items.insert(3, 25) # Insert 25 at index 3
print(items)    # Print the final elements of list.